//--------------------------------------------------------------------------------------------------------------------
// Implementation to the WATCard
//--------------------------------------------------------------------------------------------------------------------
#include "watcard.h"

//--------------------------------------------------------------------------------------------------------------------
// Constructor, when the instance is created it has a $0 balance
//--------------------------------------------------------------------------------------------------------------------
WATCard::WATCard() : balance( 0 ) {}

//--------------------------------------------------------------------------------------------------------------------
// Deposit money
//--------------------------------------------------------------------------------------------------------------------
void WATCard::deposit( unsigned int amount ) {
    balance += amount;
} // WATCard::deposit

//--------------------------------------------------------------------------------------------------------------------
// Withdraw money
//--------------------------------------------------------------------------------------------------------------------
void WATCard::withdraw( unsigned int amount ) {
    balance -= amount;
} // WATCard::withdraw

//--------------------------------------------------------------------------------------------------------------------
// determine the balance
//--------------------------------------------------------------------------------------------------------------------
unsigned int WATCard::getBalance() {
    return balance;
} // WATCard::getBalance
